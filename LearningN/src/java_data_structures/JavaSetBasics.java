package java_data_structures;

public class JavaSetBasics {

	public static void main(String[] args) {
		/*
		 * 
		 Java Set Interface:
		 - The Set interface is part of the Java Collections Framework and is used to store a 
		   collection of unique elements.
		 - Unlike a List, a Set does not allow duplicates, and it does not preserve the order of 
		   elements (unless you're using TreeSet or LinkedHashSet).
		 - Common classes that implement Set:
		   i) HashSet - fast and unordered
		   ii) TreeSet - sorted set
		   iii) LinkedHashSet - ordered by insertion
		 - Tip: Use a Set when you need to store unique values only.
		 
		 - Common Set Methods:
		 
		    | **Method**   | **Description**                                 |
			| ------------ | ----------------------------------------------- |
			| `add()`      | Adds an element if it is not already in the set |
			| `remove()`   | Removes the element from the set                |
			| `contains()` | Checks if the set contains the element          |
			| `size()`     | Returns the number of elements                  |
			| `clear()`    | Removes all elements                            |


		 - Set vs. List:
		 
		  	| **List**          | **Set**                   |
			| ----------------- | ------------------------- |
			| Allows duplicates | Does not allow duplicates |
			| Maintains order   | Does not guarantee order  |
			| Access by index   | No index-based access     |

		 
		 * */
	}

}
